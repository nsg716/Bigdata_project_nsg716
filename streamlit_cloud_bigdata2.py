def run_streamlit_app2():
    import pandas as pd
    import streamlit as st



 
    uploaded_file = st.file_uploader("CSV 파일을 업로드하세요", type='csv')
    if uploaded_file is not None:
        data = pd.read_csv(uploaded_file, encoding='cp949')
        st.write(data)



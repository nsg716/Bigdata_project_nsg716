def run_streamlit_app2():
    import pandas as pd
    import streamlit as st


    @st.cache_data  
    def load_data(url):
        df = pd.read_csv(url)
        return df
    
    df = load_data("https://github.com/nsg716/raw/master/test.csv")
    st.dataframe(df)
    
    st.button("Rerun")

